// ── Language Management ──

function getLang() {
  return localStorage.getItem('omh-lang') || 'en';
}

function setLang(lang) {
  localStorage.setItem('omh-lang', lang);
  document.documentElement.setAttribute('lang', lang);
  applyTranslations(lang);
  updateSwitcherDisplay(lang);
}

function applyTranslations(lang) {
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.getAttribute('data-i18n');
    if (translations[lang] && translations[lang][key]) {
      el.textContent = translations[lang][key];
    }
  });
  document.querySelectorAll('[data-i18n-html]').forEach(el => {
    const key = el.getAttribute('data-i18n-html');
    if (translations[lang] && translations[lang][key]) {
      el.innerHTML = translations[lang][key];
    }
  });
}

function updateSwitcherDisplay(lang) {
  const label = document.getElementById('lang-label');
  if (label) {
    label.textContent = lang === 'ko' ? '\ud55c\uad6d\uc5b4' : 'EN';
  }
  document.querySelectorAll('.lang-option').forEach(opt => {
    opt.classList.toggle('active', opt.getAttribute('data-lang') === lang);
  });
}


// ── Initialize ──
document.addEventListener('DOMContentLoaded', () => {
  const lang = getLang();
  document.documentElement.setAttribute('lang', lang);
  applyTranslations(lang);
  updateSwitcherDisplay(lang);

  const switcher = document.getElementById('lang-switcher');
  const dropdown = document.getElementById('lang-dropdown');

  if (switcher && dropdown) {
    switcher.addEventListener('click', (e) => {
      e.stopPropagation();
      dropdown.classList.toggle('show');
    });

    document.addEventListener('click', () => {
      dropdown.classList.remove('show');
    });

    dropdown.querySelectorAll('[data-lang]').forEach(opt => {
      opt.addEventListener('click', (e) => {
        e.stopPropagation();
        setLang(opt.getAttribute('data-lang'));
        dropdown.classList.remove('show');
      });
    });
  }
});
